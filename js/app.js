function getNextQuestion() {
  // Filter by role
  let pool = Q.filter(q =>
    !state.asked.has(q.id) &&
    (q.roles.includes('all') || q.roles.includes(state.role))
  );
  if (pool.length === 0) return null;

  // Prioritize weak topics at current difficulty
  let weakPool = pool.filter(q =>
    q.level === state.difficulty &&
    state.weakTopics.has(q.topic)
  );
  if (weakPool.length > 0) return weakPool[Math.floor(Math.random() * weakPool.length)];

  // Current difficulty
  let levelPool = pool.filter(q => q.level === state.difficulty);
  if (levelPool.length > 0) return levelPool[Math.floor(Math.random() * levelPool.length)];

  // Fall back to any available
  return pool[Math.floor(Math.random() * pool.length)];
}

function adjustDifficulty(correct) {
  if (correct) {
    state.consecutiveCorrect++;
    state.consecutiveWrong = 0;
    if (state.consecutiveCorrect >= 2 && state.difficulty < 4) {
      state.difficulty++;
      state.consecutiveCorrect = 0;
    }
  } else {
    state.consecutiveWrong++;
    state.consecutiveCorrect = 0;
    if (state.consecutiveWrong >= 2 && state.difficulty > 1) {
      state.difficulty--;
      state.consecutiveWrong = 0;
    }
  }
}

