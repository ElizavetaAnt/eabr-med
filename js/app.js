function selectAnswer(idx) {
  if (state.answered) return;
  state.answered = true;
  const q = state.currentQ;
  const isCorrect = idx === q.correct;

  state.answers.push({qid: q.id, chosen: idx, correct: q.correct, isCorrect, level: q.level, laws: q.laws, topic: q.topic});
  if (!isCorrect) state.weakTopics.add(q.topic);
  adjustDifficulty(isCorrect);

  document.querySelectorAll('.opt').forEach(o => o.style.pointerEvents = 'none');
  document.getElementById(`opt-${idx}`).classList.add(isCorrect ? 'correct' : 'wrong');
  if (!isCorrect) document.getElementById(`opt-${q.correct}`).classList.add('correct');

  renderExplanation(q, isCorrect);
  document.getElementById('btn-next').style.display = 'inline-block';
}

