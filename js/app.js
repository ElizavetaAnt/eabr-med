function savePDF() {
  const foxSvg = document.getElementById('fox-master').outerHTML.replace('id="fox-master"','').replace('style="display:none"','style="height:38px;width:auto;display:inline-block;vertical-align:middle"');
  const reportHtml = buildReportHTML(foxSvg);
  const date = new Date().toISOString().slice(0,10);
  const html = `<!DOCTYPE html><html lang="ru"><head><meta charset="UTF-8">
    <title>МедЗнания_${ROLE_NAMES[state.role]}_${date}</title>
    <style>*{margin:0;padding:0;box-sizing:border-box}body{background:#fff}@page{margin:10mm}@media print{body{margin:0}}</style>
  </head><body>${reportHtml}</body></html>`;
  const blob = new Blob([html], {type:'text/html;charset=utf-8'});
  const url = URL.createObjectURL(blob);
  const win = window.open(url, '_blank');
  win.addEventListener('load', () => {
    setTimeout(() => { win.print(); URL.revokeObjectURL(url); }, 400);
  });
}

function printPDF() {
  buildReportHTML();
  window.print();
}

